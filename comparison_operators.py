#Comparison operators
# (==) Equal

a = 10
b = 5
print( a==b)

# (!=) Not Equal

a = 10
b = 5
print( a!=b)

# (>) Greater than

a = 10
b = 5
print( a>b)

# (<) Less than

a = 10
b = 5
print( a<b)

# (>=) Greater than or Equal too

a = 10
b = 5
print( a>=b)

# (>=) Less than or Equal too

a = 10
b = 5
print( a<=b)

# final output would in the form of bool 
