#Logical operators
"""
1)and
2)or
3)not
"""

#and :- returns True if both statements are True

a = 10
b = 45
print(b>a and a<b)

#or :- returns True if any one statement is True

a = 10
b = 45
print(a>b or a<b)

# not:- reverse the result 

a = 10
b = 45
print(not(a<b and b>a))
